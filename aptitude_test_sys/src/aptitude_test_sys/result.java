    public result(int marks,int total,String name,String age,String phone,String qualification,String topic) {
        initComponents();
        this.setLocationRelativeTo(null);
        int per=(marks*100)/total;
        lblname.setText("Student Name : " + name);
        lblage.setText("Age : " + age);
        lblphone.setText("Phone no : " + phone);
        lblqualif.setText("Last Qualification : "+ qualification);
        lblTopic.setText("Topic : " + topic);
        lbltotal.setText("Total Marks : " + total);
        lblmarks.setText("Mars Obtained : " + marks + " out of " + total);
        lblper.setText("Percentage is : " +per);
        if(per<60)
        {
            lblgrade.setText("Failed");
        }
        else if(per>=60)
        {
            lblgrade.setText("Pass");
        }
    }

