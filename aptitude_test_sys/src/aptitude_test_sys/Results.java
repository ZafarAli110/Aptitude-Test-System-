    private void btn_show_dataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_show_dataActionPerformed
        
        if(flag.equals("Daily") && !combo_day.getSelectedItem().toString().equals("Day") && !combo_month.getSelectedItem().toString().equals("Month") && !combo_year.getSelectedItem().toString().equals("Year"))
        {
            FillTable("select * from results where date = '"+combo_year.getSelectedItem().toString()+"-"+combo_month.getSelectedItem().toString()+"-"+combo_day.getSelectedItem().toString()+"'");
        }
        else if(flag.equals("Monthly") && !combo_month.getSelectedItem().toString().equals("Month") && !combo_year.getSelectedItem().toString().equals("Year"))
        {
            FillTable("select * from results where date like '"+combo_year.getSelectedItem().toString()+"-"+combo_month.getSelectedItem().toString()+"%'");
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Select Complate Date");
        }
        
    }//GEN-LAST:event_btn_show_dataActionPerformed

    private void combo_result_typeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_combo_result_typeItemStateChanged
        if(combo_result_type.getSelectedItem().toString().equals("Daily"))
        {
            combo_day.setEnabled(true);
            combo_month.setEnabled(true);
            combo_year.setEnabled(true);
            flag="Daily";
        }
        else if(combo_result_type.getSelectedItem().toString().equals("Monthly"))
        {
            combo_day.setEnabled(false);
            combo_month.setEnabled(true);
            combo_year.setEnabled(true);
            flag="Monthly";
        }
        else
        {
            combo_day.setEnabled(false);
            combo_month.setEnabled(false);
            combo_year.setEnabled(false);
        }
    }//GEN-LAST:event_combo_result_typeItemStateChanged

